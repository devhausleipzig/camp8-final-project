import Head from "next/head";
import { useSession, signIn, signOut } from "next-auth/react";
import { redirect } from "next/navigation";
import { useRouter } from "next/router";
import axios from "axios";
import { object } from "zod";
import { AuthType } from "./api/createUser";
import { useEffect } from "react";
import { Session } from "next-auth";

async function checkOrCreateUser(object: Session) {
  await axios
    .post("http://localhost:3000/api/createUser", object)
    .then((respuesta) => console.log(respuesta));
}

export default function Home() {
  const { data: session } = useSession();
  const { push, asPath } = useRouter();
  useEffect(() => {
    if (session) {
      checkOrCreateUser(session);
    }
  }, [session]);

  const handleSignOut = () => {
    signOut({ redirect: true, callbackUrl: "/auth/signIn" });
  };
  const handleSignIn = () => push(`/auth/signIn?callbackURL=${asPath}`);
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h1 className="text-red-600 text-4xl font-bold flex justify-center">
        Landing Page
      </h1>
      <div>
        <p>Hello</p>
        {session ? (
          <div>
            <p>Signed in</p>
            <button onClick={handleSignOut}>Sign Out</button>
          </div>
        ) : (
          <div>
            <p>Not Signed In</p>
            <button onClick={() => handleSignIn()}>Sign in</button>
          </div>
        )}
      </div>
    </>
  );
}
